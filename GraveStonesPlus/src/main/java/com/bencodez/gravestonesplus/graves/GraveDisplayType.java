package com.bencodez.gravestonesplus.graves;

/**
 * Represents the visual representation used for a grave.
 */
public enum GraveDisplayType {

	/**
	 * Uses a player head (skull block) as the grave marker.
	 */
	PLAYER_HEAD,

	/**
	 * Uses a display entity for the grave marker.
	 */
	DISPLAY_ENTITY,

	/**
	 * Uses a chest block as the grave marker.
	 */
	CHEST
}